# ============================================================
# 斐波那契数列计算程序
# 功能：计算斐波那契数列，结果存储在 x3 寄存器
# ============================================================

.section .text.init
.globl _start

_start:
    # 初始化寄存器
    addi x1, x0, 0          # x1 = 0 (F(0))
    addi x2, x0, 1          # x2 = 1 (F(1))
    addi x3, x0, 0          # x3 = 结果寄存器
    addi x4, x0, 10         # x4 = 10 (计算前10个斐波那契数)

loop:
    add  x3, x1, x2         # x3 = x1 + x2
    addi x1, x2, 0          # x1 = x2
    addi x2, x3, 0          # x2 = x3
    addi x4, x4, -1         # x4 = x4 - 1 (计数器减1)
    bne  x4, x0, loop       # 如果 x4 != 0，跳转到 loop

end:
    jal  x0, end            # 无限循环（程序结束）
